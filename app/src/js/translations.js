function getRandomFact(lang) {
    const facts = organFacts[lang] || organFacts['en'];
    const randomIndex = Math.floor(Math.random() * facts.length);
    return facts[randomIndex];
}

function updatePageText(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        
        // Handle Random Fact specially
        if (key === 'random_fact') {
            el.innerText = getRandomFact(lang);
            return;
        }

        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'number' || el.type === 'password')) {
                el.placeholder = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    const langSelector = document.getElementById('languageSelector');
    if (langSelector) {
        langSelector.value = savedLang;
        langSelector.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }
    updatePageText(savedLang);
});
